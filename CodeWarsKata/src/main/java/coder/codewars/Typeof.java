package coder.codewars;

public class Typeof {
    public static void typeof(double var) {
        System.out.println("type: double, var = " + var);
    }

    public static void typeof(int var) {
        System.out.println("type: int, var = " + var);
    }

    public static void typeof(char var) {
        System.out.println("type: char, var = " + var);
    }

    public static void typeof(byte var) {
        System.out.println("type: byte, var = " + var);
    }

    public static void typeof(long var) {
        System.out.println("type: long, var = " + var);
    }

    public static void typeof(String var) {
        System.out.println("type: String, var = " + var);
    }
}
